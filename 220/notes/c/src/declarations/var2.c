// var2.c

#include <stdio.h>

int main(void) {
    int x = 1;
    double y = -1.0;

    // declare multiple variables of the same type
    int a = -5, b = 3, c = 5;
    
    printf("x = %d\n", x);
    printf("y = %f\n", y);
    printf("a = %d\n", a);
    printf("b = %d\n", b);
    printf("c = %d\n", c);
}
